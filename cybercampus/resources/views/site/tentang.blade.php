<h1>Program Studi Sistem informasi</h1>
<p>Program studi sistem informasi mulai beroperasi pada tahun 2014. Berdasarkan Keputusan Menteri Nomor: 442/E.E2/DT/2014 tanggal 19 Mei 2014, Jurusan/Program Studi Sistem Informasi sejak pendiriannya sudah berusaha menerapkan Kurikulum Berbasis Kompetensi (KBK). Saat ini berubah menjadi Kurikulum Pendidikan Tinggi (KPT) yang sesuai dengan Kerangka Kualifikasi Nasional Indonesia (KKNI). Muatan kurikulum ini terdiri dari kurikulum teori 101 sks (70%) dan kurikulum praktek 43 sks (30%) yang total berjumlah 144 SKS. Kurikulum praktek dan kurikulum teori di tetapkan berdasarkan masukan pakar, sejawat dan kalangan industri dengan penekanan pada kompetensinya.</p>
